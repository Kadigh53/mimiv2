#include "../../minishell.h"

size_t	ft_strlen(char *s)
{
	size_t	count;
	int		i;

	i = 0;
	count = 0;
	if (!s)
		return (0);
	while (s[i])
	{
		count += 1;
		i++;
	}
	return (count);
}
